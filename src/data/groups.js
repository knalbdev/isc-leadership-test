/** @returns {{ id:number, name:string, level:string, groupId:number, groupName:string }|null} */
export function findMember(id) {
  for (const g of GROUPS) {
    const m = g.members.find(m => m.id === id);
    if (m) return { ...m, groupId: g.id, groupName: g.name };
  }
  return null;
}

export const ALL_MEMBERS = GROUPS.flatMap(g =>
  g.members.map(m => ({ ...m, groupId: g.id, groupName: g.name }))
);
